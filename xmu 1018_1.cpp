#include<cstdio>

int b[2][2];

void mi(int n,int m)
{
	int c[2][2];
	b[0][0]=1;
	b[0][1]=0;
	b[1][0]=0;
	b[1][1]=1;
	int a[2][2]={1,1,1,0};
	while(n)
	{
		if(n&1)
		{
			c[0][0]=(a[0][0]*b[0][0]+a[0][1]*b[1][0])%m;
			c[0][1]=(a[0][0]*b[0][1]+a[0][1]*b[1][1])%m;
			c[1][0]=(a[1][0]*b[0][0]+a[1][1]*b[1][0])%m;
			c[1][1]=(a[1][0]*b[0][1]+a[1][1]*b[1][1])%m;
			for(int i=0;i<2;i++)
				for(int j=0;j<2;j++)
					b[i][j]=c[i][j];
			/*for(int i=0;i<2;i++)
				for(int j=0;j<2;j++)
					for(int k=0;k<2;k++)
						c[i][j]+=a[i]*/
		}
		c[0][0]=(a[0][0]*a[0][0]+a[0][1]*a[1][0])%m;
		c[0][1]=(a[0][0]*a[0][1]+a[0][1]*a[1][1])%m;
		c[1][0]=(a[1][0]*a[0][0]+a[1][1]*a[1][0])%m;
		c[1][1]=(a[1][0]*a[0][1]+a[1][1]*a[1][1])%m;
		for(int i=0;i<2;i++)
				for(int j=0;j<2;j++)
					a[i][j]=c[i][j];
		n>>=1;
		
	}
}

int main()
{
	int t,m,n;
//	mi(5,100);
//	printf("%d\n",b[0][0]);
	scanf("%d",&t);
	while(t--)
	{
		scanf("%d %d",&n,&m);
		mi(n,m);
		printf("%d\n",b[1][0]);
	}
	return 0;
}
